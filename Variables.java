public class Variables {
    public static void main(String[]Args){
        int myNum = 14;
        System.out.println(myNum);

        String myName = "Branson";
        System.out.println("My name is : " + myName);

        float pie = 3.142f;
        System.out.println( pie );

        char initials = 'A';
        System.out.println(initials);

        boolean myBool = false;
        System.out.println(myBool);

        int x = 45;
        int y = 60;
        System.out.println( x + y );

        String firstName = "Allan";
        String secondName = "Branson";
        System.out.println("My names are: "+ firstName + " " + secondName);

        /*Primitive data types
         * These are data types which when declared require the type and size specification
         * They are: int, boolean, char, float,long, double, byte
         */

         int z = 30;
         boolean bool2 = false;
         char letter = 'q';
         float gravity = 9.8f;
         double complex = 7.8900036;
         long negative = -89900;

         System.out.println(z);
         System.out.println(bool2);
         System.out.println( letter + gravity + complex + negative );


    }
}
